package com.ys168.gam.model;

public class RoomInfo {

    private int id;
    private String name;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public RoomInfo(int id, String name) {
        super();
        this.id = id;
        this.name = name;
    }
}
